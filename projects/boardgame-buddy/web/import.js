// Render a header card distinguishing new-game-creation from chunk-add-to-existing.
// `meta` is a BggGameMetaResponse (already_in_db + bgg fields).
function renderGameContextHeader(meta) {
  if (!meta) return "";
  const name = escapeHtml(meta.name || "Unknown");
  if (meta.already_in_db) {
    return `
      <div class="alert alert-info text-sm py-2 mt-2">
        <i data-lucide="library" class="w-4 h-4"></i>
        <div>
          <span class="font-semibold">Adding chunks to existing game:</span>
          ${name}
        </div>
      </div>
    `;
  }
  const players = (meta.min_players || meta.max_players)
    ? `${meta.min_players ?? "?"}–${meta.max_players ?? "?"} players`
    : "";
  const time = meta.playing_time ? `${meta.playing_time} min` : "";
  const year = meta.year_published ? `(${meta.year_published})` : "";
  const meta_line = [players, time].filter(Boolean).join(" · ");
  const desc = (meta.description || "").replace(/&#10;/g, " ").trim();
  const descShort = desc.length > 240 ? desc.slice(0, 240) + "…" : desc;
  return `
    <div class="alert alert-warning text-sm py-2 mt-2">
      <i data-lucide="sparkles" class="w-4 h-4"></i>
      <div><span class="font-semibold">Creating new game</span> from this bundle.</div>
    </div>
    <div class="card bg-base-300 mt-2">
      <div class="card-body p-3 flex-row gap-3">
        ${meta.thumbnail_url ? `
          <img src="${escapeHtml(meta.thumbnail_url)}" alt=""
               class="w-20 h-20 rounded object-cover flex-shrink-0" />
        ` : `
          <div class="w-20 h-20 rounded bg-base-200 flex items-center justify-center flex-shrink-0">
            <i data-lucide="image" class="w-6 h-6 opacity-40"></i>
          </div>
        `}
        <div class="min-w-0">
          <div class="font-bold">${name} <span class="text-xs text-base-content/60">${year}</span></div>
          ${meta_line ? `<div class="text-xs text-base-content/60 mt-0.5">${meta_line}</div>` : ""}
          ${descShort ? `<div class="text-xs text-base-content/70 mt-1 line-clamp-3">${escapeHtml(descShort)}</div>` : ""}
        </div>
      </div>
    </div>
  `;
}

async function handleImportFileSelect(e) {
  const file = e.target.files?.[0];
  const preview = document.getElementById("import-guide-preview");
  const submit = document.getElementById("import-guide-submit");
  if (!file) {
    importPendingBundle = null;
    importGameContext = null;
    bundleEditCtx = null;
    preview.innerHTML = "";
    submit.disabled = true;
    return;
  }
  try {
    const text = await file.text();
    const bundle = JSON.parse(text);
    if (!bundle.game?.bgg_id || !Array.isArray(bundle.chunks) || !bundle.chunks.length) {
      throw new Error("Missing required fields: game.bgg_id and chunks[]");
    }
    importPendingBundle = bundle;
    importGameContext = null;
    const renderAll = () => {
      const ctxHeader = renderGameContextHeader(importGameContext);
      preview.innerHTML = ctxHeader + renderBundlePreview(importPendingBundle, true);
      if (window.lucide) window.lucide.createIcons();
      submit.disabled = !importPendingBundle.chunks.length;
    };
    bundleEditCtx = { bundle: importPendingBundle, rerender: renderAll };
    renderAll();
    submit.disabled = false;
    // Fetch BGG/DB context in the background — don't block the preview.
    apiFetch(`/games/bgg-preview/${bundle.game.bgg_id}`)
      .then((meta) => {
        importGameContext = meta;
        renderAll();
      })
      .catch((err) => {
        // Soft-fail: BGG quota or network — preview still works without the header.
        console.warn("BGG preview lookup failed:", err);
      });
  } catch (err) {
    importPendingBundle = null;
    importGameContext = null;
    bundleEditCtx = null;
    preview.innerHTML = `<div class="alert alert-error text-sm"><i data-lucide="alert-circle" class="w-4 h-4"></i> ${escapeHtml(err.message)}</div>`;
    submit.disabled = true;
  }
  if (window.lucide) window.lucide.createIcons();
}

function groupBundleChunksByType(chunks) {
  const seen = new Map();
  const groups = [];
  chunks.forEach((c, i) => {
    const key = c.chunk_type || "?";
    if (!seen.has(key)) {
      seen.set(key, groups.length);
      groups.push({ label: key, items: [] });
    }
    groups[seen.get(key)].items.push({ chunk: c, index: i });
  });
  return groups;
}

function renderBundlePreview(bundle, editable = false) {
  const missing = bundle.source?.missing || [];
  const chunks = (bundle.chunks || []).slice(0, 25);
  const cols = editable ? 3 : 2;

  const groups = groupBundleChunksByType(chunks);
  const chunkRows = groups.map(g => {
    const headerRow = `<tr><td colspan="${cols}" class="text-xs font-semibold text-base-content/50 uppercase tracking-wide pt-2 pb-0.5">${escapeHtml(g.label)}</td></tr>`;
    const dataRows = g.items.map(({ chunk: c, index: i }) => `
        <tr>
          <td class="text-sm leading-tight">${escapeHtml(c.title || "(no title)")}</td>
          <td class="text-xs text-base-content/60">${((c.content || "") + "").length} chars</td>
          ${editable ? `
          <td class="text-right">
            <button class="btn btn-ghost btn-xs" onclick="openChunkEditForm(${i})" title="Edit chunk">
              <i data-lucide="pencil" class="w-3 h-3"></i>
            </button>
            <button class="btn btn-ghost btn-xs text-error" onclick="deleteBundleChunk(${i})" title="Delete chunk">
              <i data-lucide="trash-2" class="w-3 h-3"></i>
            </button>
          </td>` : ""}
        </tr>`).join("");
    return headerRow + dataRows;
  }).join("");

  const emptyRow = editable && !chunks.length
    ? `<tr><td colspan="${cols}" class="text-center text-xs text-base-content/50 py-3">All chunks removed — add at least one before importing.</td></tr>`
    : "";

  return `
    <div class="card bg-base-300 p-3 mt-2">
      <div class="flex items-center justify-between mb-2">
        <div>
          <div class="font-bold">${escapeHtml(bundle.game?.name || "Unknown")}</div>
          <div class="text-xs text-base-content/60">BGG #${escapeHtml(String(bundle.game?.bgg_id || ""))} · ${chunks.length} chunk${chunks.length === 1 ? "" : "s"}</div>
        </div>
        ${missing.length ? `<span class="badge badge-warning badge-sm">missing: ${missing.map(escapeHtml).join(", ")}</span>` : ""}
      </div>
      <div class="overflow-x-auto">
        <table class="table table-xs">
          <thead><tr><th>Title</th><th>Size</th>${editable ? "<th></th>" : ""}</tr></thead>
          <tbody>${chunkRows}${emptyRow}</tbody>
        </table>
      </div>
    </div>
  `;
}

async function handleImportSubmit() {
  if (!importPendingBundle) return;
  const submit = document.getElementById("import-guide-submit");
  submit.disabled = true;
  submit.classList.add("loading");
  try {
    const result = await apiFetch("/guides/submit", {
      method: "POST",
      body: importPendingBundle,
    });
    showToast(result.message || "Submitted for review.", "success");
    importPendingBundle = null;
    importGameContext = null;
    bundleEditCtx = null;
    document.getElementById("import-guide-file").value = "";
    document.getElementById("import-guide-preview").innerHTML = "";
  } catch (err) {
    showToast("Upload failed: " + err.message, "error");
  } finally {
    submit.classList.remove("loading");
    submit.disabled = false;
  }
}

async function downloadGuideBuilderInstructions() {
  // The canonical skill file lives at .claude/commands/guide-from-rulebook.md in
  // the repo. Vercel serves static files from the project's web/ directory, so
  // copy the file into web/ at deploy time (see build.sh / GH Actions). We fetch
  // it here as plain text and trigger a download.
  try {
    const res = await fetch("/guide-from-rulebook.md");
    if (!res.ok) throw new Error("Instructions file not found (expected /guide-from-rulebook.md)");
    const text = await res.text();
    const blob = new Blob([text], { type: "text/markdown" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = "guide-from-rulebook.md";
    document.body.appendChild(a);
    a.click();
    a.remove();
    URL.revokeObjectURL(url);
  } catch (err) {
    showToast("Download failed: " + err.message, "error");
  }
}

// ── Chunk edit / delete (shared by upload preview and admin review modal) ────

function deleteBundleChunk(index) {
  if (!bundleEditCtx) return;
  bundleEditCtx.bundle.chunks.splice(index, 1);
  bundleEditCtx.rerender();
}

async function openChunkEditForm(index) {
  if (!bundleEditCtx) return;
  const chunk = bundleEditCtx.bundle.chunks[index];
  if (!chunk) return;

  await openChunkEditorModal({
    existing: chunk,
    onSave: (data) => {
      Object.assign(bundleEditCtx.bundle.chunks[index], data);
      bundleEditCtx.rerender();
      showToast("Chunk updated", "success");
    },
  });
}

